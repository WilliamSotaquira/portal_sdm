                        <div class="box-tab">
                            <h4>Política de Seguridad y Salud en el Trabajo de la SDM</h4>

                            <div class="box-tab-img">
                                <div class="row">
                                    <div class="col-12 col-sm-8 offset-sm-2">
                                        <img alt="" class="img-fluid w-100"
                                            src="https://www.movilidadbogota.gov.co/sites/default/files/2025-11-25/_wg_secretaria%20de%20movilidad__0106.jpg">
                                    </div>
                                </div>
                                <div class="box-tab-i"><i>Foto: Secretaría Distrital de Movilidad</i></div>
                            </div>

                            <p>De acuerdo con el Decreto 672 de 2018, la Secretaría Distrital de Movilidad tiene por objeto
                                Orientar y liderar la formulación de las políticas del Sistema de Movilidad para atender los
                                requerimientos de desplazamiento de pasajeros y de carga en la zona urbana, tanto vehicular
                                como peatonal y de su expansión en el área rural del Distrito Capital en el marco de la
                                interconexión del Distrito Capital con la red de ciudades de la región central, con el país
                                y con el exterior.</p>

                            <p>Desde esta perspectiva y en armonía con el plan estratégico, la Secretaría Distrital de
                                Movilidad se compromete a proporcionar lugares de trabajo seguros y saludables, a proteger
                                la seguridad y salud de todos sus colaboradores(as), independiente de su forma de
                                contratación o vinculación, incluyendo a los contratistas y empresas tercerizadas, en sus
                                diferentes sedes, a eliminar los peligros y reducir los riesgos para la Seguridad y Salud en
                                el Trabajo mediante la implementación y la mejora continua del Sistema de Gestión de la
                                Seguridad y Salud en el Trabajo.</p>

                            <p>La alta dirección de la SDM, lidera y respalda la implementación del Sistema de Gestión de
                                Seguridad y Salud en el Trabajo, mediante la asignación de los recursos necesarios para la
                                prevención de lesiones y deterioro de la salud mediante la identificación de los peligros,
                                evaluación y valoración de riesgos, estableciendo los respectivos controles, dando
                                cumplimiento a la normatividad vigente aplicable en materia de riesgos laborales y otros
                                requisitos y mediante la consulta y la participación de los trabajadores, y sus
                                representantes.</p>

                            <p>La presente política se mantendrá documentada, será revisada y actualizada si es necesario
                                con una frecuencia mínima anual o cuando las condiciones organizacionales o legislativas lo
                                ameriten, será socializada y estará disponible a todos sus grupos de interés a través de los
                                mecanismos de comunicación que tiene la entidad.</p>
                        </div>
                    </div>

                    <div class="tab-pane fade" id="objetivos" role="tabpanel" aria-labelledby="objetivos-tab">
                        <div class="box-tab">
                            <h4>Objetivos SGSST</h4>

                            <div class="box-tab-img">
                                <div class="row">
                                    <div class="col-12 col-sm-8 offset-sm-2">
                                        <img alt="" class="img-fluid w-100"
                                            src="/web/sites/default/files/Paginas/14-12-2021/_jrg7869.webp">
                                    </div>
                                </div>
                                <div class="box-tab-i"><i>Foto: Secretaría Distrital de Movilidad</i></div>
                            </div>

                            <ol>
                                <li>Identificar continua y sistemáticamente los peligros, evaluar, valorar los riesgos en
                                    SST y determinar los controles operacionales para su eliminación o mitigación.</li>
                                <li>Prevenir lesiones y deterioro de la salud relacionados con el trabajo a los (as)
                                    colaboradores (as) proporcionando lugares de trabajo seguros y saludables, favoreciendo
                                    en todo momento su consulta y participación y la de sus representantes.</li>
                                <li>Cumplir la normatividad nacional vigente en materia de riesgos laborales y de otra
                                    índole, teniendo en cuenta los requisitos aplicables a la Secretaría.</li>
                                <li>Definir e implementar planes y estrategias para el mejoramiento continuo de las
                                    condiciones de salud y seguridad en el trabajo.</li>
                            </ol>
