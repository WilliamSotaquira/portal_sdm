        document.addEventListener('DOMContentLoaded', function() {
            // Video Gallery functionality
            document.querySelectorAll("[data-video-gallery]").forEach(function(gallery) {
                var panes = Array.from(gallery.querySelectorAll(".sgsst-video-pane"));
                var buttons = Array.from(gallery.querySelectorAll(".sgsst-video-link"));

                var pausePane = function(pane) {
                    pane.querySelectorAll("video").forEach(function(video) {
                        try {
                            video.pause();
                            video.currentTime = 0;
                        } catch (error) {}
                    });

                    pane.querySelectorAll("iframe").forEach(function(frame) {
                        var src = frame.getAttribute("src");
                        frame.setAttribute("src", src);
                    });
                };

                var setActive = function(targetButton) {
                    buttons.forEach(function(btn) {
                        var isActive = btn === targetButton;
                        btn.classList.toggle("active", isActive);
                        btn.setAttribute("aria-selected", isActive ? "true" : "false");
                        btn.tabIndex = isActive ? 0 : -1;
                    });

                    var targetSelector = targetButton.getAttribute("data-target");
                    panes.forEach(function(pane) {
                        var isTarget = "#" + pane.id === targetSelector;
                        pane.classList.toggle("active", isTarget);
                        pane.hidden = !isTarget;
                        if (!isTarget) {
                            pausePane(pane);
                        }
                    });

                    targetButton.focus();
                };

                buttons.forEach(function(button, index) {
                    button.addEventListener("click", function() {
                        if (button.classList.contains("active"))
                            return;

                        setActive(button);
                    });

                    button.addEventListener("keydown", function(event) {
                        var handled = false;
                        var nextIndex = index;
                        if (event.key === "ArrowRight" || event.key === "ArrowDown") {
                            nextIndex = (index + 1) % buttons.length;
                            handled = true;
                        } else if (event.key === "ArrowLeft" || event.key === "ArrowUp") {
                            nextIndex = (index - 1 + buttons.length) % buttons.length;
                            handled = true;
                        } else if (event.key === "Home") {
                            nextIndex = 0;
                            handled = true;
                        } else if (event.key === "End") {
                            nextIndex = buttons.length - 1;
                            handled = true;
                        }

                        if (handled) {
                            event.preventDefault();
                            setActive(buttons[nextIndex]);
                        }
                    });
                });
            });
        });
    </script>

